func assert(err error) {
	if err != nil {
		Log(slog.LevelError, "%s", err)
		panic(err)
	}
}

func cleanText(s string) string {
	s = strings.TrimPrefix(s, "#")
	s = strings.ReplaceAll(s, "\\n", "\n")
	s = regexp.MustCompile(`{LAYOUT_PC#([^}]*)}`).ReplaceAllString(s, "$1")
	s = regexp.MustCompile(`{LAYOUT_(MOBILE|PS)#[^}]*}`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`{/?LINK[^}]*}`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`</?color[^>]*>`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`{[^}]*}`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`<i>[^<]*</i>`).ReplaceAllString(s, "")
	return strings.TrimSpace(s)
}

func indentText(prefix, lines string) string {
	b := bytes.NewBuffer(nil)
	for line := range strings.Lines(lines) {
		line = strings.TrimSuffix(line, "\n")
		fmt.Fprintf(b, "%s%s\n", prefix, line)
	}
	return b.String()
}

func wrapText(s string, limit uint) string {
	b := bytes.NewBuffer(nil)
	var n uint
	for _, char := range s {
		n++
		if n > limit && unicode.IsSpace(char) {
			char = '\n'
		}
		b.WriteRune(char)
		if char == '\n' {
			n = 0
		}
	}
	return b.String()
}

func useTemplate(name string, v any) []byte {
	name = path.Join("templates", name)
	tmpl, err := embedfs.ReadFile(name)
	assert(err)
	t, err := template.New(name).Parse(string(tmpl))
	assert(err)

	var buf bytes.Buffer
	if err := t.Execute(&buf, v); err != nil {
		fmt.Println(string(tmpl))
		assert(fmt.Errorf("failed to execute template: %w", err))
	}
	return buf.Bytes()
}

func ConvertEnum[into ~int32](from any, values map[string]int32, invalid into) into {
	if v, ok := values[fmt.Sprint(from)]; ok {
		return into(v)
	}
	return invalid
}

func ConvertAddProps(in []*excel.PropValue) ([]*model.PromotionAddProp, error) {
	var out []*model.PromotionAddProp
	for _, add := range in {
		if add.Value == 0 || add.PropType == excel.FIGHT_PROP_NONE {
			continue
		}
		typ := ConvertEnum[model.StatType](add.PropType, model.StatType_value, -1)
		if typ == -1 {
			return nil, fmt.Errorf("unknown prop=%v", add.PropType)
		}
		out = append(out, &model.PromotionAddProp{
			PropType: typ,
			Value:    add.Value,
		})
	}
	return out, nil
}

func dumpGo(v any, compact bool) string {
	opts := litter.Config
	opts.Compact = compact
	opts.HideZeroValues = true
	opts.DumpRawFunc = func(v reflect.Value, w io.Writer) bool {
		type enum interface {
			Descriptor() protoreflect.EnumDescriptor
		}
		if v.Type().Implements(reflect.TypeFor[enum]()) {
			num := protoreflect.EnumNumber(v.Int())
			if num == 0 || num == -1 {
				Log(slog.LevelError, "invalid enum value: %v(%v)", v.Type(), num)
				fmt.Fprintf(w, "%s(%s)", v.Type(), v)
			} else {
				fmt.Fprintf(w, "%s_%s", v.Type(), v)
			}
			return true
		}
		return false
	}
	return opts.Sdump(v)
}

func dumpJSON(v any) ([]byte, error) {
	b := bytes.NewBuffer(nil)
	enc := json.NewEncoder(b)
	enc.SetEscapeHTML(false)
	err := enc.Encode(v)
	return b.Bytes(), err
}

func dumpYAML(v any) ([]byte, error) {
	return yaml.Dump(
		v,
		yaml.WithCompactSeqIndent(false),
		yaml.WithIndent(2),
		yaml.WithLineWidth(-1),
	)
}

func writeFile(name string, data []byte) {
	name = path.Clean(name)

	const hdr = "// Code generated by \"pipeline\"; DO NOT EDIT.\n\n"
	switch {
	case strings.HasSuffix(name, ".go"):
		data = append([]byte(hdr), data...)
		if tmp, err := format.Source(data, format.Options{
			LangVersion: runtime.Version(),
			ModulePath:  baseModule,
			ExtraRules:  true,
		}); err != nil {
			fmt.Println(string(data))
			assert(fmt.Errorf("failed to format go file: name=%v,err=%w", name, err))
		} else {
			data = tmp
		}
	case strings.HasSuffix(name, ".js"):
		data = append([]byte(hdr), data...)
	case strings.HasSuffix(name, ".json"):
		var tmp bytes.Buffer
		if err := json.Indent(&tmp, bytes.TrimSpace(data), "", strings.Repeat(" ", 2)); err != nil {
			fmt.Println(string(data))
			assert(fmt.Errorf("failed to format json file: name=%v,err=%w", name, err))
		}
		tmp.WriteString("\n")
		data = tmp.Bytes()
	}

	if old, err := projectRoot.ReadFile(name); err == nil {
		if bytes.Equal(old, data) {
			return
		}
	}
	Log(slog.LevelInfo, "writing to %s", name)
	assert(projectRoot.MkdirAll(path.Dir(name), 0o755))
	assert(projectRoot.WriteFile(name, data, 0o644))
}

func walkConfigs() ([]*Config, error) {
	var configs []*Config
	err := fs.WalkDir(projectRoot.FS(), ".", func(path string, d fs.DirEntry, err error) error {
		if err != nil || d.IsDir() {
			return err
		}
		// skip symlink
		if d.Type()&os.ModeSymlink != 0 {
			return nil
		}
		if d.Name() != "config.yml" {
			return nil
		}

		data, err := projectRoot.ReadFile(path)
		if err != nil {
			return err
		}
		var cfg *Config
		if err := yaml.Unmarshal(data, &cfg); err != nil {
			return fmt.Errorf("failed to unmarshal %v: %w", path, err)
		}
		if cfg.Use == "pipeline" {
			cfg.Path = filepath.ToSlash(path)
			cfg.ClearSpec()
			configs = append(configs, cfg)
		}
		return nil
	})
	if err != nil {
		return nil, err
	}
	slices.SortFunc(configs, func(a, b *Config) int {
		if n := cmp.Compare(a.Kind, b.Kind); n != 0 {
			return n
		}
		return cmp.Compare(a.Path, b.Path)
	})
	return configs, nil
}

func Log(level slog.Level, format string, args ...any) {
	var prefix string
	switch level {
	case slog.LevelError:
		prefix = color.RedString("error")
	case slog.LevelWarn:
		prefix = color.YellowString(" warn")
	case slog.LevelInfo:
		prefix = color.CyanString(" info")
	case slog.LevelDebug:
		prefix = "debug"
		if !optVerbose {
			return
		}
	default:
		prefix = " none"
	}
	fmt.Fprintf(os.Stderr, "%s: %s %s\n", prefix, color.New(color.Faint).Sprint("pipeline"), fmt.Sprintf(format, args...))
}
